/* 
Гадание на долларовый счет
*/

import java.io.*;

public class Solution {
    public static void main(String[] args) throws Exception {
        int x = 10;
        while (x != 0) {
            System.out.println("SSSSSSSSSS");
            x--;
        }
    }
}

